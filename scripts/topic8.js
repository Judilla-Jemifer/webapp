function startanimate() {
 document.getElementById("animationone").className += " add1";
 document.getElementById("animationtwo").className += " add2";
 document.getElementById("animationthree").className += " add3";
}

function stopanimate() {
 document.getElementById("animationone").className -= " add1";
 document.getElementById("animationtwo").className -= " add2";
 document.getElementById("animationthree").className -= " add3";
}


